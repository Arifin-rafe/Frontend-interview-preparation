// css position property values:
// 1. static - Default position; elements flow in the normal document flow.
// 2. relative - Positioned relative to its normal position; can be offset using top, right, bottom, left.
// 3. fixed - Positioned relative to the viewport; can be offset using top, right, bottom, left.
// 4. absolute - Positioned relative to the nearest positioned ancestor; can be offset using top, right, bottom, left.
// 5. sticky - Toggles between relative and fixed based on the scroll position; sticks to a defined position when scrolling past it.

// what box sizing in CSS?
