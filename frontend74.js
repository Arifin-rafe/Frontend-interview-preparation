// what are the 3 types of popups in JavaScript?
// 1. alert() - Displays a message and an OK button.
// 2. confirm() - Displays a message with OK and Cancel buttons, returning true for OK and false for Cancel.
// 3. prompt() - Displays a message prompting the user to input text, returning the input value or null if canceled.

// what are the loops in JavaScript?
// 1. for loop - Executes a block of code a specified number of times.
// 2. while loop - Executes a block of code as long as a specified condition is true.
// 3. do...while loop - Executes a block of code at least once, and then continues to execute as long as a specified condition is true.
// 4. for...in loop - Iterates over the enumerable properties of an object (keys).
// 5. for...of loop - Iterates over iterable objects (values), such as arrays and strings.

// What are HTML entities?
